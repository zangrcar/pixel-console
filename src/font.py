SLOVENIAN_BYTES = {
    0x80: "Č",
    0x81: "Š",
    0x82: "Ž",
    0x83: "č",
    0x84: "š",
    0x85: "ž",
}
SLOVENIAN_ENCODING = {char: byte for byte, char in SLOVENIAN_BYTES.items()}


def encode_text(text: str, replace_unknown: bool = False) -> bytes:
    data = bytearray()

    for char in text:
        codepoint = ord(char)

        if 0x20 <= codepoint <= 0x7E:
            data.append(codepoint)
        elif char in SLOVENIAN_ENCODING:
            data.append(SLOVENIAN_ENCODING[char])
        elif replace_unknown:
            data.append(ord("?"))
        else:
            raise ValueError(
                f"Unsupported character {char!r}; "
                "only ASCII and ČŠŽčšž are supported"
            )

    return bytes(data)


def is_text_byte_supported(value: int) -> bool:
    return 0x20 <= value <= 0x7E or value in SLOVENIAN_BYTES


@dataclass(frozen=True)
class PixelFont:
    name: str
    width: int
    height: int
    spacing: int
    line_spacing: int
    glyphs: dict[int, tuple[int, ...]]


