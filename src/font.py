def _parse_pattern(pattern: str) -> tuple[int, ...]:
    rows = pattern.split("/")

    if len(rows) != 7 or any(len(row) != 5 for row in rows):
        raise ValueError(f"Invalid font pattern: {pattern}")

    return (0, *(int(row, 2) for row in rows))


def _base_glyphs() -> dict[int, tuple[int, ...]]:
    glyphs = {ord(char): _parse_pattern(pattern) for char, pattern in _PATTERNS.items()}

    for byte, pattern in _SLOVENIAN_PATTERNS.items():
        glyphs[byte] = tuple(int(row, 2) for row in pattern.split("/"))

    return glyphs


def _tall_glyphs(glyphs: dict[int, tuple[int, ...]]) -> dict[int, tuple[int, ...]]:
    source_rows = (0, 1, 1, 2, 3, 4, 5, 6, 6, 7)

    return {
        byte: tuple(rows[source_y] for source_y in source_rows)
        for byte, rows in glyphs.items()
    }


def _wide_glyphs(glyphs: dict[int, tuple[int, ...]]) -> dict[int, tuple[int, ...]]:
    result = {}

    for byte, rows in glyphs.items():
        wide_rows = []

        for row in rows:
            wide_row = 0

            for source_x in (0, 1, 1, 2, 3, 3, 4):
                wide_row = (wide_row << 1) | ((row >> (4 - source_x)) & 1)

            wide_rows.append(wide_row)

        result[byte] = tuple(wide_rows)

    return result


def _italic_glyphs(glyphs: dict[int, tuple[int, ...]]) -> dict[int, tuple[int, ...]]:
    result = {}

    for byte, rows in glyphs.items():
        italic_rows = []

        for y, row in enumerate(rows):
            shift = 2 if y < 3 else 1 if y < 6 else 0
            italic_rows.append(row << shift)

        result[byte] = tuple(italic_rows)

    return result


def _bold_glyphs(glyphs: dict[int, tuple[int, ...]]) -> dict[int, tuple[int, ...]]:
    return {
        byte: tuple((row << 1) | row for row in rows)
        for byte, rows in glyphs.items()
    }


_CLASSIC = _base_glyphs()

FONTS = {
    0: PixelFont("Classic", 5, 8, 1, 1, _CLASSIC),
    1: PixelFont("Tall", 5, 10, 1, 1, _tall_glyphs(_CLASSIC)),
    2: PixelFont("Wide", 7, 8, 1, 1, _wide_glyphs(_CLASSIC)),
    3: PixelFont("Italic", 7, 8, 1, 1, _italic_glyphs(_CLASSIC)),
    4: PixelFont("Bold", 6, 8, 1, 1, _bold_glyphs(_CLASSIC)),
}


def get_font(font_id: int) -> PixelFont:
    if font_id not in FONTS:
        raise ValueError(f"Invalid font id: {font_id}")

    return FONTS[font_id]
